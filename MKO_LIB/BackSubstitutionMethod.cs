using System;

/// <summary>
/// Back Substitution Method
/// Розв'язує систему лінійних рівнянь з верхньотрикутною матрицею
/// 
/// Алгоритм:
/// 1. Починаючи з останнього рядка (i=n-1) і рухаючись вверх
/// 2. Для кожного рядка i:
///    - Ініціалізуємо D = 0
///    - Обчислюємо суму x[j] * a[i][j] для всіх j > i
///    - Знаходимо x[i] = (b[i] - D) / a[i][i]
/// 
/// Матриця a повинна бути верхньотрикутною (нулі під діагоналлю)
/// </summary>
public class BackSubstitutionMethod
{
    /// <summary>
    /// Розв'язує систему рівнянь A*x = b методом зворотної підстановки
    /// </summary>
    /// <param name="a">Верхньотрикутна матриця коефіцієнтів розміром n×n</param>
    /// <param name="b">Вектор вільних членів розміром n</param>
    /// <param name="n">Порядок системи (кількість рівнянь)</param>
    /// <returns>Вектор розв'язків x розміром n</returns>
    public static double[] Solve(double[][] a, double[] b, int n)
    {
        // Ініціалізуємо вектор розв'язків
        double[] x = new double[n];
        
        // Крок 1: Починаємо з останнього рядка і рухаємось вверх
        for (int i = n - 1; i >= 0; i--)
        {
            // Крок 2: Ініціалізуємо суму D для поточного рядка
            double D = 0;
            
            // Крок 3: Обчислюємо суму D = x[j] * a[i][j] для всіх j > i
            for (int j = n - 1; j > i; j--)
            {
                // Крок 4: Додаємо добуток до суми
                D = D + x[j] * a[i][j];
            }
            
            // Крок 5: Обчислюємо x[i] за формулою: x[i] = (b[i] - D) / a[i][i]
            // Перевіряємо, щоб уникнути ділення на нуль
            if (Math.Abs(a[i][i]) < 1e-10)
            {
                throw new InvalidOperationException($"Діагональний елемент a[{i}][{i}] = 0. Матриця виродилась.");
            }
            
            x[i] = (b[i] - D) / a[i][i];
        }
        
        // Повертаємо вектор розв'язків
        return x;
    }
    
    /// <summary>
    /// Виводить результати розв'язку на консоль
    /// </summary>
    /// <param name="x">Вектор розв'язків</param>
    public static void PrintSolution(double[] x)
    {
        Console.WriteLine("\nРозв'язок системи:");
        for (int i = 0; i < x.Length; i++)
        {
            Console.WriteLine($"x[{i}] = {x[i]:F6}");
        }
    }
}
